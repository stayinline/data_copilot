-- 案例 1.1：新建订单日汇总表（建表咨询）
-- 用户提问："我想在 ClickHouse 里建一张订单日汇总表，包含日期、区域、品类、订单数、
--           GMV、下单用户数，按天分区，帮我生成建表语句"
(
    order_date   Date           COMMENT '订单日期',
    region       String         COMMENT '区域',
    category     String         COMMENT '品类',
    order_count  UInt64         COMMENT '订单数',
    gmv          Decimal(18, 2) COMMENT 'GMV',
    buyer_count  UInt64         COMMENT '下单用户数'
)
ENGINE = MergeTree()
PARTITION BY toYYYYMM(order_date)
ORDER BY (order_date, region, category);

-- 案例 1.2：复杂 SQL 加工 — 多表关联查询
-- 用户提问："帮我写一条SQL，统计每个会员等级中，购买金额前10的商品品类，
--           输出等级、品类、总金额、订单数，按等级和总金额排序"
SELECT
    u.level        AS member_level,
    o.category     AS product_category,
    SUM(o.amount)  AS total_amount,
    COUNT(*)       AS order_count
FROM orders o
JOIN users u ON o.user_id = u.user_id
WHERE o.status IN ('paid', 'shipped', 'completed')
GROUP BY u.level, o.category
ORDER BY u.level, total_amount DESC
LIMIT 40;

